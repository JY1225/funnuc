CREATE TABLE
    ZONE_BOUNDARIES
    (
        ZONE_ID INTEGER, 
        BOTTOMCOORD INTEGER NOT NULL,
        TOPCOORD INTEGER NOT NULL,
        CONSTRAINT PK_BOUND_ID PRIMARY KEY (ZONE_ID),
        CONSTRAINT FK_BOTTOMCOORD_BOUND FOREIGN KEY (BOTTOMCOORD) REFERENCES COORDINATES (ID)
    ON
    DELETE
        CASCADE,
        CONSTRAINT FK_TOPCOORD_BOUND FOREIGN KEY (TOPCOORD) REFERENCES COORDINATES (ID)
    ON
    DELETE
        CASCADE
    );

